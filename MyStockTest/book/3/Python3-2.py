var1 = 'I am python!'   #利用单引号给字符串变量赋值
var2 = "您好，好久不见"         #利用双引给字符串变量赋值
print("显示var1中的字符串："+var1)             #输出字符串
print("显示var2中的字符串："+var2)
print("显示var1中第一个的字符串："+var1[0])          #输出var1中的第一个字符
print("显示var2中第三个的字符串："+var2[2])
print("显示var1中第一到第四个的字符串："+var1[0:4])
print("显示var2中第四到第六个的字符串："+var2[3:6])
